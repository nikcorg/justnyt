<h2>Miten se toimii?</h2>

<p>Just Nyt on vain välityspalvelu. Minne, sen päättää kuraattori. Kuraattori on blogien lukija, joka mielenkiintoiseen juttuun törmätessään suosittelee sitä muille Just Nytin avulla.</p>

<p>Sinulle kaikki on hyvin helppoa. Tarvitset vain yhden kirjanmerkin selaimeesi: <a href="/parhautta" rel="nofollow">justnyt.fi/parhautta</a>. Se ohjaa sinut aina suoraan viimeisimpään suositukseen. Se, kuinka usein sisältö päivittyy, riippuu  kuraattorista.</p>

<p>Suositeltujen historiaa voit selailla <a href="/historiaa">Arkisto</a>-sivulta. Kuraattorien historian näet <a href="/kuraattorit">Kuraattorit</a>-sivulta.

<p>Mikäli mielesi tekee kuraattorinhattua, ilmoittaudu <a href="/kuraattoriksi">vapaaehtoiseksi</a>. Voit myös <a href="/ilmianna-blogikirjoitus">ilmiantaa hyvän blogikirjoituksen</a>, kun sellaiseen törmäät.</p>

<p>Voit myös seurata suosituksia <a href="http://twitter.com/justnytfi">Twitterissä</a>, <a href="https://www.facebook.com/justnytfi">Facebookissa</a> ja <a href="https://plus.google.com/103263858445378300353/posts">Google Plussassa</a>.</p>

